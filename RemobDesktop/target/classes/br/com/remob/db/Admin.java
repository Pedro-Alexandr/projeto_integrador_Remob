    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
       String selectedValue = this.jComboBox1.getSelectedItem().toString();
       
       if (selectedValue.equals("Passageiros")){
           Passageiro passageiro = new Passageiro();
           this.setVisible(false);
           passageiro.setVisible(true);
       } else if (selectedValue.equals("Motoristas")){
           Motorista motorista = new Motorista();
           this.setVisible(false);
           motorista.setVisible(true);
       } else if (selectedValue.equals("Veiculos")){
           Veiculo veiculo = new Veiculo();
           this.setVisible(false);
           veiculo.setVisible(true);
       } else if (selectedValue.equals("Viagens")){
           Viagem viagem = new Viagem();
           this.setVisible(false);
           viagem.setVisible(true);
       }
    }//GEN-LAST:event_jComboBox1ActionPerformed

